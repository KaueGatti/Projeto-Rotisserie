    private void jBAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAlterarActionPerformed
        model.setValueAt(jTFNome.getText(), jTBairros.getSelectedRow(), 0);
        model.setValueAt(jTFValorEntrega.getText().replace(",", "."), jTBairros.getSelectedRow(), 1);
        Bairro bairro = new Bairro();
        bairro = model.getRowData(jTBairros.getSelectedRow());
        model.updateBairro(bairro);
        jBAlterar.setEnabled(false);
    }//GEN-LAST:event_jBAlterarActionPerformed

    private void jBRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRemoverActionPerformed
        model.deleteBairro(model.getRowData(jTBairros.getSelectedRow()), jTBairros.getSelectedRow());
        jBRemover.setEnabled(false);
        clearInputs();
    }//GEN-LAST:event_jBRemoverActionPerformed

    private void jBAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAdicionarActionPerformed
        if (!isDataEmpty()) {
            try {
                Bairro bairro = new Bairro();

                bairro.setNome(jTFNome.getText());
                bairro.setValorEntrega(Double.parseDouble(jTFValorEntrega.getText().replace(",", ".")));

                model.createBairro(bairro);

                clearInputs();
                jTFNome.requestFocus();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Erro: " + e);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Preencha todos os dados necess√°rios!");
        }
    }//GEN-LAST:event_jBAdicionarActionPerformed

    private void jTBairrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTBairrosMouseClicked
        if (jTBairros.getSelectedRow() != -1) {
            jTFNome.setText(model.getRowData(jTBairros.getSelectedRow()).getNome());
            jTFValorEntrega.setText(String.valueOf(model.getRowData(jTBairros.getSelectedRow()).getValorEntrega()));
            jBAlterar.setEnabled(true);
            jBRemover.setEnabled(true);
        }
    }//GEN-LAST:event_jTBairrosMouseClicked

    public boolean isDataEmpty() {
        for (Component comp : jPanel1.getComponents()) {
            if (comp instanceof JTextField jTextField) {
                if (jTextField.getText().equals("")) {
                    return true;
                }
            }
        }
        return false;
    }

    public void clearInputs() {
        jTFNome.setText("");
        jTFValorEntrega.setText("");
    }

